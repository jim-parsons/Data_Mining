import numpy as np

# data =[[a00, a01],
#        [a10, a11]]
# axis=0时，沿着第0个下标变化的方向进行操作，也就是a00->a10, a01->a11，也就是纵坐标的方向
# 假设axis = i，则numpy沿着第 i 个下标变化的方向进行操作

np_data = np.random.randint(0, 6, [4, 2, 3, 4])
print("==========test===============")
print(np_data)
print("==========result axis = 0 ===============")
print(np_data.sum(axis=0))
print("==========result axis = 3 ===============")
print(np_data.sum(axis=3))

# 当axis=0
# 第一个元素值=D0000+D1000+D2000+D3000=0+3+1+2=6
# 第二个元素值=D0001+D1001+D2001+D3001=5+0+1+5=11

# 当axis=3(一共四维)
# 第一个元素值=D0000+D0001+D0002+D00003=0+1+5+3=9
# 第二个元素值=D0010+D0011+D0012+D00013=5+2+5+5=17

